const numbers = [1, 2, 3, 4, 5];

// Using push() to add an element to the end of the array
numbers.push(6);
console.log(numbers);

// Output: [1, 2, 3, 4, 5, 6]

// Using push() to add multiple elements to the end of the array
numbers.push(7, 8, 9);
console.log(numbers);

// Output: [1, 2, 3, 4, 5, 6, 7, 8, 9]